    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // Windows Activated 
        get_settings get=new get_settings();
        String Activation_Key = get.Activation_Key;
        String Activation_Dir = get.Activation_Dir;   
        File file = new File (Activation_Dir);
        try {     
            if(file.exists()){                
                BufferedReader br = new BufferedReader(new FileReader(file));
                String st = br.readLine();
                if(st != null){
                   //Print ac.kp
                   //System.out.println(st);
                   if(st.equals(Activation_Key)){
                        ac_status.setText("K-POS is Activated !");
                        //Show First 8 Activation Key
                        activation_input.setText(st);
                        //Enable Import Button
                        import_button.setEnabled (true);
                        login_button.setEnabled (true);
                    }
                   else{
                       ac_status.setText("K-POS is Not Activated !");
                       import_button.setEnabled (false);
                       login_button.setEnabled (false);
                   }
                }
                else{
                    ac_status.setText("K-POS is Not Activated !");
                }              
            }
            else{
                ac_status.setText("Activation File Not Found !");
            }
        }
        catch(Exception e){
            ac_status.setText("K-POS is Not Activated !");
            System.out.println("Error with Acativation Code.");
        }
    }//GEN-LAST:event_formWindowActivated
       
