    private void active_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_active_buttonActionPerformed
        // Install Activation Key
        get_settings get=new get_settings();
        String Activation_Key = get.Activation_Key;
        String Activation_Dir = get.Activation_Dir;  
        
        //Check IF Activated or Not
        File file = new File (Activation_Dir);
        String Ac_Key = activation_input.getText();
        try {              
            //Validate Activation Key                
            if(Ac_Key.equals(Activation_Key)){
                //Write Activation Key                    
                  try{
                    PrintWriter writer = new PrintWriter(Activation_Dir, "UTF-8");
                    writer.println(Ac_Key);
                    writer.close();
                    success.setText("Successfully Activated !");  
                    ac_status.setText("K-POS is Activated !");       
                    import_button.setEnabled (true);
                    login_button.setEnabled (true);
                  }   
                  catch(Exception e){
                      System.out.println(e);
                  }
            }
            else{
                import_button.setEnabled (false);
                login_button.setEnabled (false);
                success.setText("Wrong Activation Key !");
            } 
        } catch (Exception e) {            
            System.out.println(e);
        }
    }//GEN-LAST:event_active_buttonActionPerformed

